    id                    INTEGER PRIMARY KEY,
    visit_date            TIMESTAMP NOT NULL,
    drug_combination      INTEGER,
    thermal_combination_1 INTEGER,
    thermal_combination_2 INTEGER,
    thermal_combination_3 INTEGER,
    thermal_combination_4 INTEGER,
    sensor_pain_1         INTEGER,
    sensor_pain_2         INTEGER,
    sensor_pain_3         INTEGER,
    sensor_pain_4         INTEGER,
    sensor_pain_5         INTEGER,
    sensor_pain_6         INTEGER,
    affective_pain_1      INTEGER,
    affective_pain_2      INTEGER,
    affective_pain_3      INTEGER,
    affective_pain_4      INTEGER,
    affective_pain_5      INTEGER,
    affective_pain_6      INTEGER,
    redness               VARCHAR(64),
    disorientation        VARCHAR(64),
    other_adverse         VARCHAR(64),
    other_adverse_senso   VARCHAR(64),
    study_id              INTEGER,
    FOREIGN KEY (study_id) REFERENCES baseline(id)
